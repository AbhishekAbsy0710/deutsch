#!/usr/bin/env node
/**
 * generate-registry.js
 * Phase 2: Dynamic registry generator for the German curriculum.
 *
 * Usage:
 *   node scripts/generate-registry.js
 *
 * Add to package.json:
 *   "scripts": { "generate:registry": "node scripts/generate-registry.js" }
 *
 * This script:
 *   1. Scans src/data/lessons-*.ts for all lesson files
 *   2. Parses each file to extract the exported const name
 *   3. Regenerates src/data/lessons-registry.ts with all imports + one merged export
 *
 * After migration:
 *   - Replace the manual lessons.ts spread with:
 *       import { lessonData } from "./lessons-registry";
 *   - Run `node scripts/generate-registry.js` before every push
 *   - No more manual wiring!
 */

const fs = require("fs");
const path = require("path");

const DATA_DIR = path.resolve(__dirname, "../src/data");
const OUTPUT_FILE = path.resolve(DATA_DIR, "lessons-registry.ts");

// Find all lesson module files (exclude the registry output itself and lessons.ts)
const EXCLUDE = new Set(["lessons.ts", "lessons-registry.ts"]);
const lessonFiles = fs
  .readdirSync(DATA_DIR)
  .filter((f) => f.startsWith("lessons-") && f.endsWith(".ts") && !EXCLUDE.has(f))
  .sort();

console.log(`Found ${lessonFiles.length} lesson files.`);

// Extract exported const name from each file
function extractExportName(filePath) {
  const content = fs.readFileSync(filePath, "utf8");
  // Match: export const lessonsXxxYyy: Record<...>
  const match = content.match(/export const (\w+)\s*:/);
  if (!match) {
    console.warn(`  ⚠️  No export found in ${path.basename(filePath)}, skipping.`);
    return null;
  }
  return match[1];
}

const entries = [];
for (const file of lessonFiles) {
  const fullPath = path.join(DATA_DIR, file);
  const exportName = extractExportName(fullPath);
  if (exportName) {
    const moduleName = file.replace(".ts", "");
    entries.push({ file, moduleName, exportName });
  }
}

console.log(`Generating registry for ${entries.length} modules...`);

// Build the output file
const importLines = entries
  .map((e) => `import { ${e.exportName} } from "./${e.moduleName}";`)
  .join("\n");

const spreadLines = entries.map((e) => `  ...${e.exportName},`).join("\n");

const output = `// AUTO-GENERATED by scripts/generate-registry.js
// DO NOT EDIT MANUALLY — run \`node scripts/generate-registry.js\` to regenerate.
// Last generated: ${new Date().toISOString()}
// Total lesson modules: ${entries.length}

import { Lesson } from "./lessons";

${importLines}

export const lessonData: Record<string, Lesson> = {
${spreadLines}
};

export default lessonData;
`;

fs.writeFileSync(OUTPUT_FILE, output, "utf8");

console.log(`\n✅ Registry generated: ${OUTPUT_FILE}`);
console.log(`   Modules: ${entries.length}`);
console.log(`   Estimated words: ~${entries.length * 20} (at 20 words/module average)`);

// Also output a coverage summary
const levelCounts = { A1: 0, A2: 0, B1: 0, B2: 0, C1: 0, C2: 0 };
for (const e of entries) {
  const level = e.moduleName.match(/lessons-(a1|a2|b1|b2|c1|c2)/i)?.[1]?.toUpperCase();
  if (level && levelCounts[level] !== undefined) levelCounts[level]++;
}
console.log("\nLevel distribution:");
for (const [level, count] of Object.entries(levelCounts)) {
  console.log(`  ${level}: ${count} modules (~${count * 20} words)`);
}
